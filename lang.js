let currentLang = 'en';

function applyLang(lang) {
  currentLang = lang;
  const t = I18N[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (t[k] !== undefined) el.innerHTML = t[k];
  });
  // Update html lang attr
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;
  // Update active state
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
  // Special font for Chinese
  document.body.classList.toggle('lang-zh', lang === 'zh');
}

document.addEventListener('DOMContentLoaded', () => {
  // Wire up language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });

  // NAV scroll
  const navbar = document.getElementById('navbar');
  window.addEventListener('scroll', () => navbar.classList.toggle('scrolled', window.scrollY > 60), {passive:true});

  // Scroll Reveal
  const observer = new IntersectionObserver(entries => {
    entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('visible'); });
  }, {threshold: 0.1});
  document.querySelectorAll('.reveal,.reveal-left,.reveal-right').forEach(el => observer.observe(el));

  // FAQ Accordion
  document.querySelectorAll('.faq-q').forEach(btn => {
    btn.addEventListener('click', () => {
      const item = btn.closest('.faq-item');
      const isOpen = item.classList.contains('open');
      document.querySelectorAll('.faq-item.open').forEach(i => i.classList.remove('open'));
      if (!isOpen) item.classList.add('open');
    });
  });

  // Weapons Carousel
  const track = document.getElementById('weaponsTrack');
  const dotsWrap = document.getElementById('weaponDots');
  const slides = track.querySelectorAll('.weapon-slide');
  let cur = 0; const total = slides.length;
  slides.forEach((_, i) => {
    const d = document.createElement('div');
    d.className = 'dot' + (i === 0 ? ' active' : '');
    d.addEventListener('click', () => goTo(i));
    dotsWrap.appendChild(d);
  });
  function goTo(n) {
    cur = (n + total) % total;
    track.style.transform = `translateX(-${cur * 100}%)`;
    dotsWrap.querySelectorAll('.dot').forEach((d, i) => d.classList.toggle('active', i === cur));
  }
  document.getElementById('prevWeapon').addEventListener('click', () => goTo(cur - 1));
  document.getElementById('nextWeapon').addEventListener('click', () => goTo(cur + 1));
  setInterval(() => goTo(cur + 1), 4500);

  // Parallax
  const heroBg = document.querySelector('.hero-bg');
  window.addEventListener('scroll', () => {
    if (window.scrollY < window.innerHeight) heroBg.style.transform = `translateY(${window.scrollY * 0.28}px)`;
  }, {passive:true});

  // Particles
  const canvas = document.getElementById('particles-canvas');
  const ctx = canvas.getContext('2d');
  let W, H, particles = [];
  function resize() { W = canvas.width = window.innerWidth; H = canvas.height = window.innerHeight; }
  resize(); window.addEventListener('resize', resize);
  function rand(a, b) { return a + Math.random() * (b - a); }
  class Particle {
    constructor() { this.reset(); this.y = rand(0, H); }
    reset() { this.x=rand(0,W); this.y=H+10; this.vx=rand(-.4,.4); this.vy=rand(-.7,-.2); this.size=rand(1,2.5); this.life=rand(.4,1); this.decay=rand(.002,.005); this.isRed=Math.random()<.5; }
    update() { this.x+=this.vx; this.y+=this.vy; this.life-=this.decay; if(this.life<=0||this.y<-10) this.reset(); }
    draw() { ctx.save(); ctx.globalAlpha=this.life*.6; ctx.fillStyle=this.isRed?`rgba(200,16,46,${this.life})`:`rgba(212,160,23,${this.life})`; ctx.beginPath(); ctx.arc(this.x,this.y,this.size,0,Math.PI*2); ctx.fill(); ctx.restore(); }
  }
  for (let i = 0; i < 75; i++) { const p = new Particle(); p.y = rand(0, H); particles.push(p); }
  (function loop() { ctx.clearRect(0,0,W,H); particles.forEach(p=>{p.update();p.draw();}); requestAnimationFrame(loop); })();

  // Apply default language
  applyLang(currentLang);
});
